                      <p class="intro-below">The Process of PT Participation</p>
                      <p class="intro-border-below"></p>
                      <p class="intro-text">
                        <ul class="listStyle">
                          <li><p>Each participant will be given PT samples together with sample receipt form, instructions to laboratories and results sheet form.Participants are required to carry out analysis/ testing using method that are routinely used in their laboratories. Duration of analysis is one (1) month from the date of sample dispatch.  The test results must be sent to DOC before the closing date. </p></li>
                          <li><p>The DOC will evaluate all test data using ‘robust statistics’ and comparison of results from each participant laboratory to the ‘consensus value’ (robust mean) of the entire group according to international standards.</p></li>
                          <li><p>During registration of the PT Programme, participants will be given a secret code number of which laboratory performance in PT reports will be referred to.  This code number is only being known to the particular laboratory and relevant personnel of the PT Programme.</p></li>
                          <li><p>For laboratories in Malaysia which has been accredited under SAMM MS ISO/IEC 17025, the laboratory performance will be notified to the Department of Standards Malaysia (DSM) if requested.   However, this is subject to the approval by the laboratory which will be obtained in advance.</p></li>         
                        </ul>
